let i = 1;                 // ვქმნით ცვლადს და ვანიჭებთ საწყის მნიშვნელობას
while (i <= 10) {          // ვამოწმებთ პირობას: სანამ i ნაკლებია ან ტოლია 10-ის
  console.log(i);          // ვბეჭდავთ i-ს
  i++;                     // ვზრდით i-ს მნიშვნელობას ერთით
}



