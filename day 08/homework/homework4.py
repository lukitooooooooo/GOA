#For loop არის ერთ-ერთი ძირითადი სტრუქტურა რომელსაც ვიყენებთ ბევრი ქმედებების შესასრულებლად.
#for loop იტევს შემდეგ კომპონენტებს:


#ex:
for i in range(5):
    print(i)

#range(5) - range(5) ნიშნავს, რომ ციკლი შესრულდება 5-ჯერ, რადგან range იწყება 0-დან და მიდის 4-ის ჩათვლით (5-ს არ ითვლის).
#i - თითოეული დაბრუნებული მნიშვნელობა (0, 1, 2, 3, 4) შეინახება  i-ში.
#print(i) - ეს  აჩვენებს i-ს მნიშვნელობას.